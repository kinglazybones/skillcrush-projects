weather = 'sunny'

case weather 
when 'windy'
	puts "You best put some weights in your pockets!"
when 'rainy'
	puts "Pack a snorkel. It's a wet one."
when 'cloudy'
	puts "Layers. Always layers."
when 'sunny'
	puts "Don't forget your SPF 1200!"
when 'snowy'
	puts "Bring a carrot for that snowman you're going to build!"
else 
	puts "Hmm, I don't have any advice for that kind of weather. Good luck!'

end
